
public abstract class Pizza {
    
    /**
     * Metodo de regresa el tipo de queso de una pizza
     * @return Tipo de Queso
     */
    public abstract String getQueso();
    
    /**
     * Metodo de regresa el tipo de masa de una pizza
     * @return Tipo de masa
     */
    public abstract String getMasa();
    
    /**
     * Metodo de regresa el tipo de carne de una pizza
     * @return Tipo de carne
     */
    public abstract String getCarne();
    
    /**
     * Metodo de regresa el costo de una pizza
     * @return Costo de la pizza
     */
    public abstract int getCosto();
    
}
